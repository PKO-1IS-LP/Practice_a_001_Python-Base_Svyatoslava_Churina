def функция(x, y):
    # x**2 + y**2 всегда неотрицательно, поэтому abs() не нужен.
    # Возвращает True, если точка внутри круга или на его границе.
    return (x**2 + y**2) <= 1

x = float(input("Введите число x:"))
y = float(input("Введите число y:"))

if функция(x, y):
    print("YES")
else:
    print("NO")