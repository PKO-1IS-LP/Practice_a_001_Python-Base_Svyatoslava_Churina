i = int(input("Введите число: "))
b = 0 # запоминающие коробочки
c = 0
p = 0
for a in range(1,i + 1): # переборка всех чисел от 1 до i
    if a % 2 == 0:
        b = b + 1
    if a % 3 == 0:
        c = c + 1
    if a % 2 == 0 and a % 3 == 0:
        p = p + 1
print("На 2: ", b,"На 3: ",c,"На 2 и 3: ", p)